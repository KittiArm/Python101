# Compound Assignment

# set variable
x = 10
print("Before update :", x)

# normal assignment
x = x + 5
print("After update :", x)

# compound assginment
x += 5
print("After Compound Assignment (plus) :", x)

x -= 5
print("After Compound Assignment (minuse) :", x)

x *= 2
print("After Compound Assignment (times) :", x)

x /= 30
print("After Compound Assignment (division) :", x)
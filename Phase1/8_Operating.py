# Operating >> Operator | Operand

x = 10
y= 4

# + Addition
print("ผลบวก :", x + y)

# - Subtraction
print("ผลลบ :", x - y)

# * Multiplication
print("ผลคูณ :", x * y)

# / Division
print("ผลหาร :", x / y)

# ** Exponential
print("ยกกำลัง :", x ** y)

# % Modulus
print("หารเอาเศษ :", x % y)

# // Fool division
print("หารปัดเศษ :", x // y)